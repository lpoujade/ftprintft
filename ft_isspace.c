#include "libft.h"

int		ft_isspace(int c)
{
	int ret;

	ret = 0;
	if (c == '\t' || c == '\n' || c == ' ' || c == '\v' || c == '\f' \
			|| c == '\r')
		ret = 1;
	return (ret);
}
