#include "libft.h"

int		ft_pow(int n, int p)
{
	int tmp;

	if (p)
	{
		tmp = n;
		while (p - 1)
		{
			n = n * tmp;
			p--;
		}
		return (n);
	}
	else
		return (1);
}
